
public class Clase1 {

    private int numero1;
    private int numero2;
    public Clase1(){
        this.numero1=0;
        this.numero2=0;
    }
    public int getNumero1(){
        return this.numero1;
    }
    public int cambio1Suma(){
        return this.numero1+this.numero2;
    }
    public int cambio2Resta(){
        return this.numero1-this.numero2;
    }
    public int cambio3Multiplicar(){
        return this.numero1*this.numero2;
    }
}
